# What are exceptions?
# There are two types of errors: Syntax errors and exceptions.
# (1). Syntax errors are errors that are caused by developers. Syntax errors can be caused
# by a misspelling or a typo in the code. Syntax errors are usually fixed by the IDE.
# (2). Exceptions are errors that need to be handled. Exception errors happen during
# code execution and they can easily go unoticed by the untrained eye, but exceptions
# needs to be handled by the developer. They need to deal with any potential issues
# and the codebase to keep the application from failing.
# Example of an exception being thrown
# a = 5 / 0 ; this is a zero division error
#
# Note 1; By default Python includes many exception errors that you can use to pick
# up potential issues in your code. ##
